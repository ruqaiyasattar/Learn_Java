public class p_inc_dec {
    public static void main(String[] args) {
        int j,i=5;
        j=i++;
        System.out.println(j+":"+i);//j=5
        j=i++;
        System.out.println(j+":"+i);//j=6
        int b,a=8;
        b=++a;
        System.out.println(b+":"+a);//j=5
        b=++a;
        System.out.println(b+":"+a);
    }
}
